public class SimplifiedPlayerException extends Exception {

    public SimplifiedPlayerException() {
    }

    public SimplifiedPlayerException(String message) {
        super(message);
    }

    public SimplifiedPlayerException(String message, Throwable cause) {
        super(message, cause);
    }

    public SimplifiedPlayerException(Throwable cause) {
        super(cause);
    }

}
